#include "ft_printf.h"

int	len_num(int n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		i++;
	}
	while (n > 9)
	{
		i++;
		n /= 10;
		ft_putstr("*");
	}
	i++;
	return (i);
}
